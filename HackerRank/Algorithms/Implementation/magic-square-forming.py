s = list(map(int, (input() + ' ' + input() + ' ' + input()).split()))
ars = [[2, 7, 6, 9, 5, 1, 4, 3, 8],
        [2, 9, 4, 7, 5, 3, 6, 1, 8],
        [4, 3, 8, 9, 5, 1, 2, 7, 6],
        [4, 9, 2, 3, 5, 7, 8, 1, 6],
        [6, 1, 8, 7, 5, 3, 2, 9, 4],
        [6, 7, 2, 1, 5, 9, 8, 3, 4],
        [8, 1, 6, 3, 5, 7, 4, 9, 2],
        [8, 3, 4, 1, 5, 9, 6, 7, 2]]
cost = 9 * 9
for ar in ars:
    cost = min(cost, sum(map(lambda x: abs(x[0] - x[1]), zip(ar, s))))
